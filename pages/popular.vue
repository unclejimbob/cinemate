<template>
  <div>
    <section>
      <h1
        class="mb-4 text-4xl font-extrabold leading-none tracking-tight text-gray-900 md:text-5xl lg:text-6xl dark:text-white"
      >
        Popular Movies
      </h1>
      <ul class="grid grid-cols-5 gap">
        <li v-for="mov in popularMovies">
          <MovieCard
            :poster_path="mov.poster_path"
            :title="mov.title"
            :id="mov.id"
            :overview="mov.overview"
            :release_date="mov.release_date"
            :popularity="mov.popularity"
            :item="mov"
          ></MovieCard>
        </li>
        <br /><br />
      </ul>
    </section>
    <section>
      <h1
        class="mb-4 text-4xl font-extrabold leading-none tracking-tight text-gray-900 md:text-5xl lg:text-6xl dark:text-white"
      >
        Popular TV Series
      </h1>
      <ul class="grid grid-cols-5 gap">
        <li v-for="tvSeries in popularSeries">
          <SeriesCard
            :poster_path="tvSeries.poster_path"
            :name="tvSeries.name"
            :id="tvSeries.id"
            :overview="tvSeries.overview"
            :first_air_date="tvSeries.release_date"
            :popularity="tvSeries.popularity"
            :item="tvSeries"
          ></SeriesCard>
        </li>
        <br /><br />
      </ul>
    </section>
  </div>
</template>

<script lang="ts" setup>
const popularMovies = useState(() => []);
const popularSeries = useState(() => []);

const { data } = await useFetch("/api/movies/popular", {
  transform: (data) => {
    popularMovies.value = data.popularMovies.results;
    popularSeries.value = data.popularSeries.results;
  },
});
</script>

